const DICTS: Record<Lang, Dict> = { en, es, pt, de, ja, ko };

function format(str: string, params?: Record<string, string | number>) {
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (params[k] != null ? String(params[k]) : `{${k}}`));
}

interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
}

const Ctx = createContext<I18nCtx | null>(null);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLangState] = useState<Lang>(() => storage.getSettings().language ?? "en");

  // Listen for cross-component settings changes
  useEffect(() => {
    const onStorage = () => {
      const l = storage.getSettings().language ?? "en";
      setLangState(l);
    };
    window.addEventListener("storage", onStorage);
    window.addEventListener("lockin:lang", onStorage as EventListener);
    return () => {
      window.removeEventListener("storage", onStorage);
      window.removeEventListener("lockin:lang", onStorage as EventListener);
    };
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    const s = storage.getSettings();
    storage.saveSettings({ ...s, language: l });
    window.dispatchEvent(new Event("lockin:lang"));
  }, []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>) => {
      const dict = DICTS[lang] ?? en;
      const str = dict[key] ?? en[key] ?? key;
      return format(str, params);
    },
    [lang]
  );

  const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);
  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
};

export const useI18n = () => {
  const c = useContext(Ctx);
  if (!c) throw new Error("useI18n must be used within I18nProvider");
  return c;
};

export const useT = () => useI18n().t;
